	public static class Car extends AJsonWrapper {

		public Car(PageRef page, String manufacturer, String brand, String model, String type,
				Integer productionBeginYear) {
			putMandatory("pageRef", page);
			putMandatory("manufacturer", manufacturer);
			putMandatory("brand", brand);
			putMandatory("model", model);
			putMandatory("type", type);
			json.put("productionBeginYear", productionBeginYear);
		}

		public Car(PageRef page) {
			putMandatory("pageRef", page);
		}

		public Car(JsonObject json) {
			super(json);
		}

		public Integer getProductionBeginYear() {
			return json.getInteger("productionBeginYear");
		}

		public String getType() {
			return json.getString("type");
		}

		public String getManufacturer() {
			return json.getString("manufacturer");
		}

		public String getBrand() {
			return json.getString("brand");
		}

		public String getModel() {
			return json.getString("model");
		}

		public String getLabel() {
			String brand = getBrand();
			if (brand == null) return getPageRef().getTitle();
			return brand + " " + getModel();
		}

		public PageRef getPageRef() {
			return getWrapper("pageRef", PageRef.class);
		}

		@Override
		public String toString() {
			return getLabel();
		}
	}


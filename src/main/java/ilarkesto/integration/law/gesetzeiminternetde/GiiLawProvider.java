	@Override
	public BookIndex loadPrepackagedBookIndex() {
		JsonObject json = JsonObject.loadResource("/GiiBookIndex.json", getClass(), false);
		if (json == null) {
			log.warn("Resource does not exist: /GiiBookIndex.json");
			return null;
		}
		return new BookIndex(json);
	}

	@Override
	protected Book loadBook(BookRef bookRef) {
		if (bookRef == null) return null;

		String reference = bookRef.getJson().getString("giiReference");
		if (reference == null)
			throw new DataLoadFailedException("Book reference has no giiReference property: " + bookRef.getJson(), null);

		File tempDir = getTempBookDataDir(bookRef.getCode());
		downloader.downloadZipAndExtract(reference + "/xml.zip", tempDir);
		String data = loadXmlFileFromDir(tempDir);

		Book book = new Book(bookRef);
		try {
			new GiiBookXmlParser(data).parseInto(book);
		} catch (ParseException ex) {
			throw new RuntimeException("Parsing book failed: " + bookRef, ex);
		}

		File dir = getBookDataDir(bookRef.getCode());
		IO.delete(dir);
		IO.move(tempDir, dir);

		return book;
	}

	String getGiiReference(String bookCode) {
		String s = GII_REFERENCES.get(bookCode);
		if (s != null) return s;
		s = bookCode.toLowerCase();
		s = s.replace('ä', '_');
		s = s.replace('ü', '_');
		s = s.replace('ö', '_');
		s = s.replace('/', '_');
		s = s.replace('ß', '_');
		s = s.replace('§', '_');
		s = s.replace('-', '_');
		s = s.replace(' ', '_');
		return s;
	}

	@Override
	public AFileStorage getDataStorage() {
		return dataStorage;
	}

	private String loadXmlFileFromDir(File dir) {
		for (File file : dir.listFiles()) {
			if (file.getName().endsWith(".xml")) return IO.readFile(file, IO.UTF_8);
		}
		throw new RuntimeException("Missing XML file in downloaded ZIP: " + dir);
	}

	public File getBookDataDir(String bookCode) {
		return dataStorage.getFile(bookCode);
	}

	private File getTempBookDataDir(String bookCode) {
		return dataStorage.getFile("/~" + bookCode);
	}

	@Override
	protected BookIndex loadBookIndex() {
		BookIndex index = new BookIndex("DE", "Deutsches Bundesrecht");
		for (int i = 'A'; i <= 'Z'; i++) {
			if (i == 'X') continue;
			loadBooks(index, String.valueOf((char) i));
		}
		for (int i = 1; i <= 9; i++) {
			loadBooks(index, String.valueOf(i));
		}
		return index;
	}

	private void loadBooks(BookIndex index, String teilliste) throws DataLoadFailedException {
		String path = "Teilliste_" + teilliste + ".html";
		String data = downloader.downloadText(path, charset);
		Parser parser = new Parser(data);

		try {
			parser.gotoAfter("<div id=\"container\">");
			while (parser.gotoAfterIf("<p><a href=\"./")) {
				String reference = parser.getUntil("/index.html\"");
				parser.gotoAfter("<abbr title=\"");
				String title = parser.getUntil("\"");
				parser.gotoAfter("\">");
				String code = parser.getUntil("<").trim();
				code = Html.convertHtmlToText(code);

				// prevent duplicates
				if (reference.equals("aeg")) {
					code = "AEG 1951";
				} else if (reference.equals("altfrg")) {
					code = "AltFRG";
				} else if (reference.equals("kraftfausbv_2001")) {
					code = "KraftfAusbV";
				} else if (reference.equals("entgbv")) {
					code = "EntgBV";
				} else if (reference.equals("ewo_bk")) {
					code = "EWO BK";
				} else if (reference.equals("tspv_2003")) {
					code = "TspV 2003";
				} else if (reference.equals("wtosa")) {
					code = "WTOSa";
				} else if (reference.equals("stvo_2013")) {
					code = "StVO";
				} else if (reference.equals("dtag_bertrano_2013_2")) {
					code = "DTAGÜbertrAnO 2013";
				} else if (reference.equals("rstruktfv")) {
					code = "RStruktFV 2011";
				}

				code = code.replace("/", "");
				code = code.replace(".", "");
				code = code.replace("-", "");
				code = code.replace("§", "");
				code = code.replace(" ", "");

				BookRef book = new BookRef(code, title);
				book.getJson().put("giiReference", reference);
				index.addBook(book);
			}
		} catch (ParseException ex) {
			throw new DataLoadFailedException("Parsing 'Teilliste' failed: " + path, ex);
		}
	}

